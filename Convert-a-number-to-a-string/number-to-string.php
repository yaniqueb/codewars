<?php
// We need a function that can transform a number (integer) into a string.
// What ways of achieving this do you know? 

//Solution 1: Using the built-in strval() function
function numberToString($num) {
  return strval($num);
}

//examples
echo strval(100);    // "100"
echo strval(999);    // "999"
echo strval(-100);   // "-100"


